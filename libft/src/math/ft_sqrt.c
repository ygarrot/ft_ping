#include "libft.h"

double ft_sqrt(double n)
{
	double	x;
	double	x0;
	double	delta;


	x0 = n;
	while (1)
	{
		x = x0 - (x0 * x0 - n) / (2 * x0);
		delta = (x - x0) / x0;
		if (delta < .000001 && delta > -.000001)
			return x;
		x0 = x;
	}
	return (1.0);
}
